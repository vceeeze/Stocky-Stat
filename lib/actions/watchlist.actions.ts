import { Watchlist } from "@/database/models/watchlist.model";
import { connectToDatabase } from "@/database/mongoose";
import { error } from "console";

export async function getWatchlistSymbolsByEmail(email: string) {
    if (!email) return [];

    try {
        const mongoose = await connectToDatabase();
        const db = mongoose.connection.db;

        if(!db) throw new Error('MongoDB connection not found')
        
            // Better Auth stores users in the "user" collection

            const user = await db.collection('user').findOne<{_id?: unknown; id?: string; email?: string}>({email: email});

            if (!user) return []
            
            const userId = (user.id as string) || String(user._id || '');
            if (!userId) return [];

            const items = await Watchlist.find({userId}, { symbol: 1}).lean();
            return items.map((i) => String(i.symbol).toLocaleLowerCase());
    } catch (err) {
        console.error('getWatchlistSymbolsByEmail error', err)
    }
}